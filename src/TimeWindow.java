public class TimeWindow {
    private int start;
    private int end;

    // This class is used to store the start and end time of a time window.
    public TimeWindow(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
